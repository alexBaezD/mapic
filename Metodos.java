class Metodos{
	
private String name;
private String value;
//*******************************************
public Metodos(String name, String value){
	this.name = name;
	this.value = value;
}
//*******************************************
public void setvalue(String value){
this.value=value;
}
//*******************************************
public String getvalue(){
  return value;
}
//*******************************************
public void setname(String name){
this.name=name;
}
//*******************************************
public String getname(){
  return name;
}
}